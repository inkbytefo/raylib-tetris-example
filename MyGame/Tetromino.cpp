std::vector<std::pair<int,int>> Tetromino::GetCells() const
{
    int typeIndex = static_cast<int>(type);
    auto& offsets = SHAPES[typeIndex][rotation];
    
    std::vector<std::pair<int,int>> cells;
    cells.reserve(4);

    for (auto& [col, row] : offsets)
    {
        cells.push_back({ x + col, y + row });
    }
    return cells;
}

void Tetromino::RotateCW()
{
    rotation = (rotation + 1) % 4;
}

void Tetromino::RotateCCW()
{
    rotation = (rotation + 3) % 4; // -1 mod 4
}

